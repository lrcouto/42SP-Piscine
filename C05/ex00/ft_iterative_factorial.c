int		ft_iterative_factorial(int nb)
{
	int n;
	int ret;

	n = 1;
	ret = 1;
	if ((nb < 0) || (nb > 12))
		return (0);
	while (n <= nb)
	{
		ret = ret * n;
		n++;
	}
	return (ret);
}
